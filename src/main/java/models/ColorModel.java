package models;

public class ColorModel {
    private int R;
    private int G;
    private int B;

    public int getG() {
        return G;
    }

    public void setG(int g) {
        G = g;
    }

    public int getB() {
        return B;
    }

    public void setB(int b) {
        B = b;
    }

    public int getR() {

        return R;
    }

    public void setR(int r) {
        R = r;
    }
}
